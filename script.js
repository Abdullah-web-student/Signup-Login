$(document).ready(function() {
    $('#login').click(function() {
        $('.login').css("background-color", "#051F20")
        $('.signup').css("background-color", "rgba(0, 0, 0, 0.30)")
    })

    $('#signup').click(function() {
        $('.signup').css("background-color", "#051F20")
        $('.login').css("background-color", "rgba(0, 0, 0, 0.30)")
    })
})
